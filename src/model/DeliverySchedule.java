package model;

public class DeliverySchedule {
private String deliveryType;
private Integer deliveryMark;
private Integer deliveryHour;

public String getDeliveryType() {
	return deliveryType;
}
public void setDeliveryType(String deliveryType) {
	this.deliveryType = deliveryType;
}
public Integer getDeliveryMark() {
	return deliveryMark;
}
public void setDeliveryMark(Integer deliveryMark) {
	this.deliveryMark = deliveryMark;
}
public Integer getDeliveryHour() {
	return deliveryHour;
}
public void setDeliveryHour(Integer deliveryHour) {
	this.deliveryHour = deliveryHour;
}
}
